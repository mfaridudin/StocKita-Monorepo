<script>
    document.addEventListener('DOMContentLoaded', () => {
        const track = document.getElementById('carouselTrack');
        let slides = document.querySelectorAll('.carousel-slide');
        const dots = document.querySelectorAll('.carousel-dot');
        const prevBtn = document.querySelector('.carousel-prev');
        const nextBtn = document.querySelector('.carousel-next');

        let index = 1;
        let slideWidth = slides[0].clientWidth;
        let autoPlayInterval;
        let isTransitioning = false;

        // Clone first and last slides for infinite loop
        const firstSlide = slides[0].cloneNode(true);
        const lastSlide = slides[slides.length - 1].cloneNode(true);

        track.appendChild(firstSlide);
        track.insertBefore(lastSlide, slides[0]);
        slides = document.querySelectorAll('.carousel-slide');

        // Initial position
        gsap.set(track, {
            x: -slideWidth
        });

        function updateDots() {
            dots.forEach((dot, i) => {
                const isActive = i === (index - 1 + dots.length) % dots.length;
                dot.style.backgroundColor = isActive ? '#ffffff' : 'rgba(255,255,255,0.4)';
                dot.style.transform = isActive ? 'scale(1.5)' : 'scale(1)';
            });
        }

        function moveToSlide(targetIndex) {
            if (isTransitioning) return;
            isTransitioning = true;

            gsap.to(track, {
                x: -(slideWidth * targetIndex),
                duration: 0.8,
                ease: "power3.inOut",
                onComplete: () => {
                    // Seamless infinite loop
                    if (targetIndex === slides.length - 1) {
                        gsap.set(track, {
                            x: -slideWidth
                        });
                        index = 1;
                    } else if (targetIndex === 0) {
                        gsap.set(track, {
                            x: -(slideWidth * (slides.length - 2))
                        });
                        index = slides.length - 2;
                    } else {
                        index = targetIndex;
                    }
                    isTransitioning = false;
                    updateDots();
                }
            });
        }

        function nextSlide() {
            moveToSlide(index + 1);
        }

        function prevSlide() {
            moveToSlide(index - 1);
        }

        // Event listeners
        nextBtn.addEventListener('click', nextSlide);
        prevBtn.addEventListener('click', prevSlide);

        dots.forEach((dot, i) => {
            dot.addEventListener('click', () => {
                moveToSlide(i + 1);
                resetAutoPlay();
            });
        });

        // Autoplay
        function startAutoPlay() {
            autoPlayInterval = setInterval(nextSlide, 5000);
        }

        function resetAutoPlay() {
            clearInterval(autoPlayInterval);
            startAutoPlay();
        }

        const container = document.querySelector('.carousel-container');
        container.addEventListener('mouseenter', () => clearInterval(autoPlayInterval));
        container.addEventListener('mouseleave', startAutoPlay);

        // Responsive
        window.addEventListener('resize', () => {
            slideWidth = slides[0].clientWidth;
            gsap.set(track, {
                x: -(slideWidth * index)
            });
        });

        // Touch/swipe support
        let startX = 0;
        let currentX = 0;

        container.addEventListener('touchstart', (e) => {
            startX = e.touches[0].clientX;
        });

        container.addEventListener('touchmove', (e) => {
            currentX = e.touches[0].clientX;
        });

        container.addEventListener('touchend', (e) => {
            const diff = startX - currentX;
            if (Math.abs(diff) > 50) { // Minimum swipe distance
                if (diff > 0) {
                    nextSlide();
                } else {
                    prevSlide();
                }
                resetAutoPlay();
            }
        });

        // Keyboard navigation
        document.addEventListener('keydown', (e) => {
            if (e.key === 'ArrowRight') {
                nextSlide();
                resetAutoPlay();
            } else if (e.key === 'ArrowLeft') {
                prevSlide();
                resetAutoPlay();
            }
        });

        // Initialize
        startAutoPlay();
        updateDots();
    });
</script>

